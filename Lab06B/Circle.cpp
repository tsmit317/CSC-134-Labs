#include "Circle.h"

///////////////////////////////////////////////////////////////////////////////////////////////////
//
// Member-function:ostream &operator << (ostream &output, Circle &c)
//
// Definition: overloaded stream Insertion Operator
//
// Returns: output
//
//////////////////////////////////////////////////////////////////////////////////////////////////

ostream &operator << (ostream &output, Circle &c)
{
    output << "radius = " << c.radius << " x = " << c.getXCoor() << " y = " << c.getYCoor() << endl;
    
    return output;
}



// constructor
Circle::Circle(int x, int y, double rad)
: Point(x,y), Shape("Circle")
{
    setRadius(rad);
    cout << "Circle Constructor called: radius = " << radius << " x = " << getXCoor() << " y = " << getYCoor() << endl;

}

// destructor
Circle::~Circle()
{
    cout << "Circle Destructor called: radius = " << radius << " x = " << getXCoor() << " y = " << getYCoor() << endl;
    
}

///////////////////////////////////////////////////////////////////////////////////////////////////
//
// Member-function: void setRadius(double rad)
//
// Definition: Sets radius equal to rad.
//
// Returns: none
//
//////////////////////////////////////////////////////////////////////////////////////////////////

void Circle::setRadius(double rad)
{
    radius = rad;
    
}

///////////////////////////////////////////////////////////////////////////////////////////////////
//
// Member-function: double getRadius()
//
// Definition: Gets the radius
//
// Returns: radius
//
//////////////////////////////////////////////////////////////////////////////////////////////////

double Circle::getRadius()
{
    return radius;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
//
// Member-function: double getArea()
//
// Definition: Calculates the area of the Circle.
//
// Returns: area
//
//////////////////////////////////////////////////////////////////////////////////////////////////

double Circle::getArea()
{
    double area = 0.0;
    area = (PI * radius * radius);
    
    return area;
}

